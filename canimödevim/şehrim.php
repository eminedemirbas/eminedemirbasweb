<div class="özellik">
  <h3>BALIKESİR</h3>
  Balıkesir, Türkiye'nin bir ili ve en kalabalık on yedinci şehri.
 Marmara Bölgesi'nin Güney Marmara Bölümü'nde, topraklarının bir kısmı
  ise Ege Bölgesi'nde yer alan ilin hem Marmara hem de Ege Denizi'ne kıyısı bulunur.
 Türkiye genelinde ise iki deniz ile komşu olan sadece 6 il vardır.
  İl, Kuzeybatı Anadolu'da bulunmaktadır. Doğusunda Bursa ve Kütahya illeri,
   güneyinde Manisa ve İzmir illeri ve batısında Çanakkale ili vardır. Ayvalık
    ilçesinden de Yunanistan'ın Midilli Adası'na komşudur.

2019 Yılı verilerine göre nüfusu 1.228.620’dir.
 İlin yüzölçümü 14.583 m²'dir. İlde km²'ye 82 kişi düşmektedir.
  Yoğunluğun en fazla olduğu ilçe, 254 kişi ile Karesi'dir.
   İlin denizden yüksekliği 145 metredir.
    2016 yılında TÜİK verilerine göre 20 ilçe ve belediye,
     bu belediyelerde toplam 1129 mahalle bulunmaktadır.


</div>

<div class="mekan">
  <h4 id="cunda" >CUNDA</h4>
  Ayvalık’a bağlı bir ada olan Cunda,
   Ege Denizi’nde yer alan eski bir Rum kasabası…
    Rumca “kokulu ada” Moshinos adıyla anılan Cunda Adası,
     bugün Kurtuluş Savaşı kahramanı olan Yarbay Ali Çetinkaya
      anısına Alibey Adası ismini almış. 1920’lerin başında 
      adadaki Rum nüfus, Giritli Müslümanlar ile mübadele edilmiş
      .Ayvalık’a göre daha sakin bir tatil beldesi olan Cunda 
      Adası, Ayvalık’a bir köprüyle bağlanıyor. Rıhtımda yer 
      alan feribot iskelesi çevresinde şekillenen Cunda
       çarşısı ve Cunda’nın en meşhur balık lokantaları 
       yine sahilde yan yana diziliyor.


</div>

<div class="mekan">
  <h4 id="ayvalık">ŞEYTAN SOFRASI</h4>
  Balıkesir'de görülmesi gereken yerlerden birisi de Ayvalık
   ilçe merkezinin 10 km güney batısında bulunan,
    Küçükköy mahallesine bağlı olan Şeytan Sofrası.
     Hem hikayesi hem de muhteşem manzarası ile insanları 
     büyüleyen Şeytan Sofrası, gün doğumu ve gün batımını 
     izlemek için mükemmel bir yer. Akşam olurken gökyüzünün 
     aldığı renkleri seyretmek çoğu insana huzur verdiği için 
     o saatlerde aşırı bir kalabalık söz konusu oluyor.
   O yüzden biraz daha erken gidip yer tutmakta fayda var.
</div>

<div class="mekan">
  <h4 id="kuşcenneti" >KUŞ CENNETİ</h4>
  Kuş Cenneti, Türkiye'de özellikle de çeşitli göçmen
   kuşların meraklıları olan kişilerin ilgisini çeken 
   bir alan olarak öne çıkmaktadır. Hatta yurtdışından
    dahi kuş türlerine ilgi duyan kişilerin ve kuş 
    gözlemcilerinin yılın belirli dönemlerinde ziyaret 
    ettiği Kuş Cenneti,
   dünya çapında önemli bir noktadır. 266 çeşit kuş türünü barındıran
   kuş cenneti müthiş bir ekosisteme ev sahipliği yapar.

</div>

<div class="mekan">
  <h4 id="kyzikos" >KYZİKOS ANTİK KENTİ</h4>
  Gerek İstanbul’ u zaptetmek için Kyzikos’a gelen Araplar,
   gerekse depremler yüzünden zarar gören Kyzikos; 23 Eylül
    1063’de olan korkunç deprem neticesinde baştan başa yıkıldı.
     Sağ kalanlar ise şehri terk etmeye başladılar. Kapıdağ 
     Yarımadası’ nı, anakaraya bağlayan berzah, ilk çağda dolmadan 
     önce bu kent,üç limanı, Hadrian Tapınağı ve amphitheatırı
      ile ünlü bir ticaret şehri ve şarap, zeytinyağı, 
      buğday,mermer, parfümeri ile ünlü bir koloniydi.
       Üç büyük deprem, berzahtaki limanın dolması ve bataklığın
        sıtma yaratması, halkının bu günkü Erdek’ e göç etmesine yol
         açtı. Depremler Efes görkemindeki 
  bu kentin bilinmezliğe terk edilmesine sebep olmuştur.





